-(int)getLastZeroCountAndPointRange:(NSRange)range
{
    int lastZeroCount = 0;
    for (int i = (int)self.length-1; i >= range.location; --i) {
        char c = [self characterAtIndex:i];
        if (c == '0' || c == '.') {
            lastZeroCount++;
        }
        else
        {
            break;
        }
    }
    return lastZeroCount;
}


-(NSString*)floatStringWithMaxBit:(int)maxBit
{
    NSRange range = [self rangeOfString:@"."];
    if (range.location == NSNotFound) {
        return self;
    }
    else
    {
        int length = (int)self.length - (int)range.location-1;
        if (length <= maxBit) {
            return self;
        }
        else
        {
            int newLength = (int)range.location+maxBit+1;
            return [self substringWithRange:NSMakeRange(0, newLength)];
        }
    }
}

-(NSString*)removeZeroString
{
    NSRange range = [self rangeOfString:@"."];
    if (range.location == NSNotFound) {
        return self;
    }
    else
    {
        int zeros = [self getLastZeroCountAndPointRange:range];
        int newLength = (int)self.length-zeros;
        if (newLength == 0) {
            return @"0";
        }
        return [self substringWithRange:NSMakeRange(0, newLength)];
    }
}

-(NSString*)miniBitStr:(int)miniBit
{
    if (miniBit == 0) {
        return self;
    }
    NSRange range = [self rangeOfString:@"."];
    if (range.location == NSNotFound) {
        NSMutableString* x = [NSMutableString stringWithString:self];
        [x appendString:@"."];
        for (int i = 0; i < miniBit; ++i) {
            [x appendString:@"0"];
        }
        return [x copy];
    }
    else
    {
        //小数个数
        int length = (int)self.length - (int)range.location-1;
        int addCount = miniBit - length;
        NSMutableString* x = [NSMutableString stringWithString:self];
        if (addCount > 0) {
            for (int i = 0; i < addCount; ++i) {
                [x appendString:@"0"];
            }
        }
        return x;
    }
}

//minbit 优先级 大于removeZero
-(NSString*)floatStringWithMaxBit:(int)maxBit minBit:(int)minBit removeZero:(BOOL)removeZero
{
    NSString* result = self;
    //number类型转成int
    if ([result isKindOfClass:[NSNumber class]]) {
        result = [NSString stringWithFormat:@"%@",result];
    }
    //不是string return nil
    if (![result isKindOfClass:[NSString class]]) {
        return nil;
    }
    //限制最大长度
    result = [result floatStringWithMaxBit:maxBit];
    //是否去除多余的0
    if (removeZero) {
        result = [result removeZeroString];
    }
    //限制最小长度
    
    //由于限制最小长度跟去除多余的0冲突，所以规定 限制最小长度优先级大于去除多余的0
    return [result miniBitStr:minBit];
}



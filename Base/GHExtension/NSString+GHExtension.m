        NSRange range = [self rangeOfString:number options:NSCaseInsensitiveSearch range:NSMakeRange(location+1, self.length-location-1)];
        [atStr addAttribute:(NSString *)NSForegroundColorAttributeName
                      value:color
                      range:range];
        if(addLinks)
        {
            [atStr addAttribute:NSLinkAttributeName
                          value:[NSURL URLWithString:[NSString stringWithFormat:@"%d",i]]
                          range:range];
        }
        if (ranges) {
            [ranges addObject:[NSValue valueWithRange:range]];
        }
        location = range.location + range.length;
        i++;
    }
    return [atStr copy];
}

